-- 道路関連と思われる事業を、事業名や目的からキーワードで抽出し、
-- 予算額の大きい順にリストアップする
SELECT
    概要.予算事業ID,
    概要.事業名,
    組織.府省庁,
    予算."計（歳出予算現額合計）" AS 予算額
FROM
    "基本情報_事業概要等" AS 概要
JOIN
    "基本情報_組織情報" AS 組織 ON 概要.予算事業ID = 組織.予算事業ID
JOIN
    "予算・執行_サマリ" AS 予算 ON 概要.予算事業ID = 予算.予算事業ID
WHERE
    (
        概要.事業名 LIKE '%道路%' OR
        概要.事業名 LIKE '%高速道路%' OR
        概要.事業名 LIKE '%国道%' OR
        概要.事業名 LIKE '%インフラ%' OR
        概要.事業の目的 LIKE '%道路%' OR
        概要.事業の目的 LIKE '%交通網%'
    )
    AND 予算."計（歳出予算現額合計）" > 0
ORDER BY
    予算額 DESC;