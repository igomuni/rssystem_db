-- 特定の事業について、全体の予算額と個別の支出額を比較検証する
SELECT
    予算."事業名",
    予算."計（歳出予算現額合計）" AS 事業全体の予算額,
    支出."支出先ブロック名",
    支出."支出先名",
    支出."金額" AS 個別の支出額,
    -- 支出額が予算額の何パーセントを占めるかを計算
    CAST(支出."金額" AS DOUBLE) / 予算."計（歳出予算現額合計）" * 100 AS 予算比率
FROM
    "予算・執行_サマリ" AS 予算
JOIN
    "支出先_支出情報" AS 支出
    ON 予算.予算事業ID = 支出.予算事業ID
WHERE
    -- ここで気になる事業の予算事業IDを指定
    予算.予算事業ID = 7259
ORDER BY
    個別の支出額 DESC;